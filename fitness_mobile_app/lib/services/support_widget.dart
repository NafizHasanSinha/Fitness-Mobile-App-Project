import 'package:flutter/material.dart';

class AppWidget {
  static TextStyle healineTextstyle(double size) {
    return TextStyle(
      color: Colors.black,
      fontSize: size,
      fontWeight: FontWeight.bold,
    );
  }

  static TextStyle mediumTextstyle(double size) {
    return TextStyle(
      color: Colors.black87,
      fontSize: size,
      fontWeight: FontWeight.w500,
    );
  }
}
